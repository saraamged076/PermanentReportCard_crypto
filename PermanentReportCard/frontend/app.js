let contract = new web3.eth.Contract(abi, contractAddress);

// ===== SETUP =====
async function setup() {
    let user = localStorage.getItem("user");

    let admin = await contract.methods.getAdmin().call();

    if (user.toLowerCase() === admin.toLowerCase()) {
        document.getElementById("title").innerText = "Admin 👑";
        document.getElementById("userMenu").style.display = "none";
    } else {
        document.getElementById("title").innerText = "User 👤";
        document.getElementById("adminMenu").style.display = "none";
    }
}

setup();

// ===== USER =====
async function viewGrade() {
    let addr = document.getElementById("student").value;

    let res = await contract.methods.getGrade(addr).call();

    document.getElementById("result").innerText =
        `Name: ${res[0]} | Grade: ${res[1]}`;
}

async function checkBalance() {
    let addr = document.getElementById("student").value;

    let coin = await contract.methods.getBalance(addr).call();
    let eth = await web3.eth.getBalance(addr);

    document.getElementById("result").innerText =
        `ETH: ${web3.utils.fromWei(eth)} | Coin: ${coin}`;
}

// ===== ACTIVITY ===== (زي بتاعك)
async function activity() {
    document.getElementById("result").innerText =
        "Activity shown in terminal (Web3 scan)";
}

// ===== ADMIN =====
async function addGrade() {
    let user = localStorage.getItem("user");

    let student = document.getElementById("student").value;
    let grade = document.getElementById("grade").value;

    let name = await contract.methods.userNames(student).call();

    await contract.methods.setGrade(student, name, grade)
        .send({ from: user });

    alert("Grade Added!");
}

async function updateGrade() {
    let user = localStorage.getItem("user");

    let student = document.getElementById("student").value;
    let newGrade = document.getElementById("newGrade").value;

    let name = await contract.methods.userNames(student).call();

    await contract.methods.setGrade(student, name, newGrade)
        .send({ from: user });

    alert("Grade Updated!");
}

async function mint() {
    let user = localStorage.getItem("user");

    let student = document.getElementById("student").value;
    let amount = document.getElementById("amount").value;

    await contract.methods.mint(student, amount)
        .send({ from: user });

    alert("Coins Minted!");
}